int main(int ac, char **av)
{
    int i;
    int j;
    i = 0;
    if((ac != 5 || ac != 6) && (av[1] >= '1' && av[1] <= '200'))
        return (-1);
    while(av[i])
    {
        j = 0;
        while(av[i][j])
        {
            if(av[i][j] >= '0' && av[i][j] <= '9')
                j++;
            else
                return (-1);
        }
        i++;
    }
    return (0);
}